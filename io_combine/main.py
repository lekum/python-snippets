sample = "En un lugar de la mancha de cuyo nombre no quiero acordarme".split()

def combine(source, maxsize):
    parts = []
    size = 0
    for part in source:
        parts.append(part)
        size += len(part)
        if size > maxsize:
            yield ''.join(parts)
            parts = []
            size = 0
    yield ''.join(parts)

for part in combine(sample, 10):
    print(part)
